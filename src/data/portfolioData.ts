// Função para buscar projeto por ID
export const getProjectById = (id: number): PortfolioItem | undefined => {
  return portfolioItems.find(item => item.id === id);
};

// Função para buscar projeto por projectId (formato "01", "02", etc.)
export const getProjectByProjectId = (projectId: string): PortfolioItem | undefined => {
  return portfolioItems.find(item => item.projectId === projectId);
};

// Função para buscar projeto por slug
export const getProjectBySlug = (slug: string): PortfolioItem | undefined => {
  return portfolioItems.find(item => item.slug === slug);
};

// Função para filtrar projetos por categoria
export const getProjectsByCategory = (category: string): PortfolioItem[] => {
  return portfolioItems.filter(item => item.category === category);
};

// Função para ordenar projetos por projectId
export const getProjectsSortedByProjectId = (): PortfolioItem[] => {
  return [...portfolioItems].sort((a, b) => {
    const aNum = parseInt(a.projectId);
    const bNum = parseInt(b.projectId);
    return aNum - bNum;
  });
};

// Função para ordenar projetos por categoria e depois por ID
export const getProjectsSortedByCategoryAndId = (): PortfolioItem[] => {
  // Definir a ordem das categorias
  const categoryOrder = {
    "Empreendimentos Verticais": 1,
    "Empreendimentos Horizontais": 2,
    "Loteamentos": 3
  };

  return [...portfolioItems].sort((a, b) => {
    // Primeiro, ordenar por categoria
    const aCategoryOrder = categoryOrder[a.category as keyof typeof categoryOrder] || 999;
    const bCategoryOrder = categoryOrder[b.category as keyof typeof categoryOrder] || 999;
    
    if (aCategoryOrder !== bCategoryOrder) {
      return aCategoryOrder - bCategoryOrder;
    }
    
    // Se as categorias são iguais, ordenar por projectId
    // Função para extrair número do projectId (considerando formatos como "01", "h01", "L01")
    const extractNumber = (projectId: string): number => {
      const match = projectId.match(/\d+/);
      return match ? parseInt(match[0]) : 0;
    };
    
    const aNum = extractNumber(a.projectId);
    const bNum = extractNumber(b.projectId);
    
    return aNum - bNum;
  });
};

// Função para obter todas as categorias únicas
export const getUniqueCategories = (): string[] => {
  const categories = portfolioItems.map(item => item.category);
  return [...new Set(categories)];
};